class Solution {
  public:
    // Function to count the number of subarrays which adds to the given sum.
    int subArraySum(vector<int>& arr, int tar) {
        int count = 0;
        unordered_map<int, int> m;
        int prefix_sum = 0;
        for(auto x : arr){
            prefix_sum += x;
            m[prefix_sum]++;
            
            if(prefix_sum - tar == 0){
                count++;
            }else{
                count += m[prefix_sum-tar];
            }
        }
        return count;
    }
};

